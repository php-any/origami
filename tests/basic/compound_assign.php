<?php

echo "=== 复合赋值运算符测试 ===\n";

// +=
$x = 10;
$x += 5;
if ($x == 15) {
    Log::info("+= 测试通过");
} else {
    Log::fatal("+= 测试失败, 实际 {$x}");
}

// -=
$y = 20;
$y -= 8;
if ($y == 12) {
    Log::info("-= 测试通过");
} else {
    Log::fatal("-= 测试失败, 实际 {$y}");
}

// *=
$z = 6;
$z *= 7;
if ($z == 42) {
    Log::info("*= 测试通过");
} else {
    Log::fatal("*= 测试失败, 实际 {$z}");
}

// /=
$a = 100;
$a /= 4;
if ($a == 25) {
    Log::info("/= 测试通过");
} else {
    Log::fatal("/= 测试失败, 实际 {$a}");
}

// %=
$b = 17;
$b %= 5;
if ($b == 2) {
    Log::info("%= 测试通过");
} else {
    Log::fatal("%= 测试失败, 实际 {$b}");
}

// .= (字符串拼接赋值)
$s = "Hello";
$s .= " World";
if ($s == "Hello World") {
    Log::info(".= 字符串拼接赋值测试通过");
} else {
    Log::fatal(".= 测试失败, 实际 '{$s}'");
}

// **= (幂运算赋值)
$p = 2;
$p **= 3;
if ($p == 8) {
    Log::info("**= 幂运算赋值测试通过");
} else {
    Log::fatal("**= 测试失败, 实际 {$p}");
}

// &= (按位与赋值)
$ba = 0b1100;
$ba &= 0b1010;
if ($ba == 0b1000) {
    Log::info("&= 按位与赋值测试通过");
} else {
    Log::fatal("&= 测试失败, 实际 {$ba}");
}

// |= (按位或赋值)
$bo = 0b1100;
$bo |= 0b0011;
if ($bo == 0b1111) {
    Log::info("|= 按位或赋值测试通过");
} else {
    Log::fatal("|= 测试失败, 实际 {$bo}");
}

// ^= (按位异或赋值)
$bx = 0b1100;
$bx ^= 0b1010;
if ($bx == 0b0110) {
    Log::info("^= 按位异或赋值测试通过");
} else {
    Log::fatal("^= 测试失败, 实际 {$bx}");
}

// 复合赋值链式使用
$c = 10;
$c += 5;
$c *= 2;
$c -= 3;
if ($c == 27) {
    Log::info("复合赋值链式使用测试通过");
} else {
    Log::fatal("复合赋值链式使用测试失败, 实际 {$c}");
}

// 复合赋值在循环中
$sum = 0;
for ($i = 1; $i <= 10; $i++) {
    $sum += $i;
}
if ($sum == 55) {
    Log::info("循环中复合赋值测试通过");
} else {
    Log::fatal("循环中复合赋值测试失败, 实际 {$sum}");
}

echo "=== 复合赋值运算符测试完成 ===\n";
